# Importamos el módulo math para utilizar la función sqrt()
import math

# Asignamos el valor 81 a la variable "y"
y = 81

# Calculamos la raíz cuadrada de la variable "y" utilizando la función sqrt()
# y asignamos el resultado a la variable "raiz_cuadrada"
raiz_cuadrada = math.sqrt(y)

# Imprimimos el resultado en pantalla
print("La raíz cuadrada de", y, "es:", raiz_cuadrada)