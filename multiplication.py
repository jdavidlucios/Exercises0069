# Asignamos los valores 20 y 30 a las variables "a" y "b", respectivamente
a = 20  # Variable "a" con valor 20
b = 30  # Variable "b" con valor 30

# Realizamos la multiplicación de las variables "a" y "b"
# y almacenamos el resultado en la variable "resultado"
resultado = a * b  # Multiplicación de "a" y "b"

# Imprimimos el resultado de la multiplicación en pantalla
print(f"El resultado de la multiplicación de {a} y {b} es: {resultado}")  # Impresión del resultado